    {
        $user = User::select('*')
            ->where([
                "type" => "5"
            ])
            ->withCount('courseParticipant')
            ->whereHas('courseParticipant', function ($query) use ($course_id) {
                $query->where(['course_id' => $course_id]);
            })
            ->paginate();
        return response()->json($user);
    }

